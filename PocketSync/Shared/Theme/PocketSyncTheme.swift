import SwiftUI

enum PocketSyncTheme {
    static let screenBackground = Color.white
    static let card = Color.white
    static let panel = Color(uiColor: .systemGray6)
    static let paper = panel
    static let ink = Color(uiColor: .label)
    static let secondaryText = Color(uiColor: .secondaryLabel)
    static let tertiaryText = Color(uiColor: .tertiaryLabel)
    static let accent = Color(uiColor: .systemBlue)
    static let positive = Color(uiColor: .systemGreen)
    static let warning = Color(uiColor: .systemOrange)
    static let rose = Color(uiColor: .systemPink)
    static let coral = warning
    static let moss = positive
    static let gold = accent
    static let blush = rose
    static let line = Color(uiColor: .separator)
    static let shadow = Color.black.opacity(0.04)
}
